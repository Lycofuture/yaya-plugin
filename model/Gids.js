export default new class Gids {
    gid(num) {
        switch (num) {
            case '1':
                return '崩坏三'
            case '2':
                return '原神'
            case '3':
                return '崩坏学园2'
            case '4':
                return '未定事件簿'
            case '5':
                return '大别野'
            case '6':
                return '崩坏星穹铁道'
            case '8':
                return '绝零区'
            default:
                return '社区版块'
        }
    }
}